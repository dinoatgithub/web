//++ increment by 1
//-- decrement by 1
// Postfix and Prefix
// ++var    pre increment(increases & returns)
//var++     post increment(returns & increases)
// --var    pre decrement(decreases & returns)
//var--     post decrement(returns & decreases)

a = 100;
console.log(++a);
console.log(a);
console.log(a++);
console.log(a);

a = 100;
console.log(--a);
console.log(a);
console.log(a--);
console.log(a);